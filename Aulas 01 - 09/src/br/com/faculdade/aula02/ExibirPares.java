/**
 * Exercício 3
 * Mostrar todos os números pares de 1 até 500
 * 
 * Date    05/09/2019
 * @author Leandro
 */
public class ExibirPares {
    
    public static void main(String[] args) {
        
        for (int i = 2; i <= 500; i += 2) {
                System.out.println(i);                
        }        
    }    
}
